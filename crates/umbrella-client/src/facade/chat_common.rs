/// Внутренний helper: routes facade-уровневое текстовое сообщение через
/// активный gateway connection. Если `core.gateway()` is `None` —
/// возвращает legacy stub `MessageId([0u8; 16])`, сохраняя
/// backwards-compat для test fixtures и facade integration tests, которые
/// не bootstrap'ят networking (см. `tests/facade_integration.rs` etc.).
///
/// Если `core.gateway()` is `Some` — отправляет
/// `ClientPayload::SendMessage { to_user_id: chat_id.bytes, ciphertext: text.bytes }`
/// через `GatewayConnection::send_envelope`, ждёт ответный envelope,
/// требует `ServerPayload::SendAck { msg_id }`, декодирует 32-char hex →
/// 16 байт → возвращает `MessageId`. Server-side errors маршрутизируются
/// в `ClientError::Network` с диагностическим кодом.
///
/// ## Session-3 ограничения (полностью out-of-scope)
///
/// - **MLS AEAD encryption**: ciphertext в session 3 — placeholder
///   `text.into_bytes()`. Реальный MLS encrypt появится в session 5
///   (create_group + add_member) когда `UmbrellaGroup` будет wired в
///   facade. Mock gateway accept'ит любые байты — wire round-trip
///   exercise валидный.
/// - **Length-hiding padding** (umbrella_padding bucket selection):
///   защищает от traffic-analysis side-channel; layer вставится в
///   session 5+ как фильтр между MLS encrypt и transport send.
/// - **Cloud-wrap (для CloudChat)**: 3-of-5 Sealed Server message-key
///   wrap. Session 6 deliverable per pipeline plan.
/// - **Sealed-sender (для SecretChat)**: anonymous credential envelope.
///   Session 7 deliverable.
/// - **to_user_id маршрутизация**: session 3 использует `chat_id.0` как
///   to_user_id placeholder. Production должен map'ить chat_id →
///   recipient peer_id(s) через group membership. Wired в session 5+
///   когда group state существует.
///
/// Все ограничения tracked в `docs/audits/phd-b-pass5-remediation-2026-05-19.md`
/// F-CLIENT-FACADE-1 milestone closure roadmap.
///
/// Internal helper: routes a facade-level text message through the active
/// gateway connection. If `core.gateway()` is `None`, returns the legacy
/// stub `MessageId([0u8; 16])` for backwards-compat with tests that do not
/// bootstrap networking. When `Some`, sends
/// `ClientPayload::SendMessage` and decodes the server's
/// `SendMessageAck.msg_id` (32-char hex → 16 bytes) into `MessageId`.
///
/// Session-3 placeholders (out-of-scope for this session, tracked under
/// F-CLIENT-FACADE-1 roadmap): MLS AEAD encrypt (ciphertext is raw text
/// bytes), length-hiding padding, Cloud-wrap, sealed-sender,
/// chat_id → peer_id routing.
///
/// [`CloudChat`]: crate::facade::CloudChat
/// [`SecretChat`]: crate::facade::SecretChat
pub(crate) async fn send_mls_text(
    core: &Arc<ClientCore>,
    chat_id: ChatId,
    text: String,
) -> Result<MessageId> {
    let Some(gateway) = core.gateway().await else {
        // Legacy / Block-7.2 facade test fixtures path — no networking
        // wired. Keep the historical stub return so existing integration
        // tests (facade_integration.rs etc.) stay green until they are
        // updated to bootstrap a mock gateway.
        return Ok(MessageId([0u8; 16]));
    };

    // F-CLIENT-FACADE-1 session 5: real MLS encrypt iff an MLS group is
    // registered for `chat_id` in `ClientCore.groups`. Backwards compat:
    // если группа не зарегистрирована (e.g. test через `CloudChat::open(
    // ChatId([0u8; 32]))` без предварительного `create`), сохраняем
    // legacy path text-as-bytes — session 3 wire-format остаётся
    // exercised mock'ом.
    //
    // F-CLIENT-FACADE-1 session 5: real MLS encrypt when a group is
    // registered; otherwise raw text bytes (legacy/test backwards compat).
    let ciphertext = if let Some(group_arc) = core.get_group(chat_id).await {
        let mut group = group_arc.lock().await;
        // MLS AEAD encrypt + signing — sync. Lock держим only during
        // crypto, не во время network I/O. Result Vec<u8> освобождает
        // lock после `?`.
        // MLS AEAD encrypt + sign — sync; the lock is dropped before
        // network I/O.
        group.encrypt_application(
            core.mls_provider.as_ref(),
            core.mls_keystore.as_ref(),
            text.as_bytes(),
        )?
    } else {
        text.into_bytes()
    };

    let payload = ClientPayload::SendMessage {
        to_user_id: chat_id.0.to_vec(),
        ciphertext,
    };
    gateway.send_envelope(payload).await.map_err(|e| {
        ClientError::Network(format!("send_envelope: {e}"))
    })?;
    let frame = gateway.recv_envelope().await.map_err(|e| {
        ClientError::Network(format!("recv_envelope: {e}"))
    })?;
    match frame.payload {
        ServerPayload::SendAck { msg_id } => decode_server_msg_id(&msg_id),
        ServerPayload::Error { code } => Err(ClientError::Network(format!(
            "gateway rejected SendMessage with code: {code}"
        ))),
        other => Err(ClientError::Network(format!(
            "expected SendAck after SendMessage, got: {other:?}"
        ))),
    }
}

/// Внутренний helper: drain ALL pending `IncomingMessage` envelopes из
/// gateway recv side, переводит каждое в [`DecryptedMessage`] и возвращает
/// список. Если `core.gateway()` is `None` — возвращает пустой `Vec`
/// (legacy stub поведение для test fixtures без networking).
///
/// Petля:
///   - `tokio::time::timeout(drain_budget, gateway.recv_envelope())`
///   - `Ok(Ok(ServerPayload::IncomingMessage{...}))` → конвертируем,
///      добавляем в result, продолжаем
///   - `Ok(Ok(other))` → не наше — log + continue (production refactor
///      session 7+ wires multiplexing routing layer)
///   - `Ok(Err(_))` либо timeout → break, return result
///
/// ## Session-4 ограничения (полностью out-of-scope)
///
/// - **Real MLS decrypt**: `text` поле в session 4 — placeholder
///   `String::from_utf8_lossy(&ciphertext).into_owned()`. Реальный MLS
///   decrypt с handle ratchet появится в session 5 когда `UmbrellaGroup`
///   будет wired.
/// - **Sender identity verification**: session 4 кладёт первые 32 байта
///   `from_user_id` напрямую в `PeerId` без KT proof lookup. Real KT
///   self-monitoring + 3-of-5 witness sig wiring — session 8.
/// - **Recv-side multiplexing**: gateway recv mutex shared с `send_text` —
///   concurrent fetch_inbox + send_text serialise друг друга через Mutex.
///   Background-task multiplexer с per-seq oneshot channels — session 7+.
///
/// Internal helper: drains ALL pending `IncomingMessage` envelopes from the
/// gateway recv side, translates each to `DecryptedMessage`, and returns
/// the list. Returns empty `Vec` when `core.gateway()` is `None` (legacy
/// stub for test fixtures without networking).
///
/// Session 4 placeholders (out-of-scope): real MLS decrypt (session 5+),
/// sender identity KT verification (session 8), recv-side multiplexing
/// for concurrent fetch_inbox + send_text (session 7+).
pub(crate) async fn fetch_mls_inbox(
    core: &Arc<ClientCore>,
    chat_id: ChatId,
) -> Result<Vec<DecryptedMessage>> {
    let Some(gateway) = core.gateway().await else {
        return Ok(Vec::new());
    };

    // F-CLIENT-FACADE-1 session 5: pre-fetch group state once (avoid
    // re-locking ClientCore.groups RwLock per envelope). MLS decrypt
    // attempts iff group is Some AND `process_incoming` succeeds — иначе
    // fallback на legacy UTF-8 lossy путь (test fixtures без real MLS
    // ciphertext).
    //
    // F-CLIENT-FACADE-1 session 5: pre-fetch group state once. MLS decrypt
    // attempted iff a group is registered AND `process_incoming` succeeds;
    // otherwise legacy UTF-8 lossy fallback.
    let group_opt = core.get_group(chat_id).await;

    let mut out = Vec::new();
    loop {
        let recv_future = gateway.recv_envelope();
        match tokio::time::timeout(FETCH_INBOX_DRAIN_TIMEOUT, recv_future).await {
            Ok(Ok(frame)) => match frame.payload {
                ServerPayload::IncomingMessage {
                    from_user_id,
                    ciphertext,
                    sent_ts_ms,
                    msg_id,
                } => {
                    let message_id = decode_server_msg_id(&msg_id)?;
                    let sender = parse_peer_id_from_bytes(&from_user_id)?;
                    let text =
                        decrypt_text_with_fallback(core, group_opt.as_ref(), &ciphertext).await;
                    out.push(DecryptedMessage {
                        message_id,
                        chat_id,
                        sender,
                        timestamp: sent_ts_ms,
                        text,
                    });
                }
                // Non-inbox envelopes can arrive on shared recv side when
                // concurrent send/auth is in flight. Session-4 strict
                // serialisation: tests sequence operations, so a stray
                // SendAck or Pong here is unexpected — log and drop. The
                // session 7+ multiplexer will route these to their
                // waiters.
                // Stray non-inbox envelope on shared recv — log and drop.
                _other => {
                    tracing::debug!(
                        "fetch_mls_inbox: discarded non-IncomingMessage envelope from shared recv"
                    );
                }
            },
            Ok(Err(e)) => {
                return Err(ClientError::Network(format!(
                    "fetch_mls_inbox: gateway recv failed: {e}"
                )));
            }
            Err(_timeout) => return Ok(out),
        }
    }
}

/// F-CLIENT-FACADE-1 session 5 helper: попытка MLS-decrypt `ciphertext` если
/// `group` is `Some`, иначе UTF-8 lossy. Honest fallback на decrypt failure:
/// логирует в tracing::debug и возвращает UTF-8 lossy. Это **transitional**
/// поведение session 5 — будущий session 7+ tightens до strict-MLS-only
/// (CommitApplied + ProposalQueued также handle).
///
/// Возвращает plaintext String. MLS payload UTF-8 lossy-decoded аналогично
/// legacy fallback path — application-level encoding выбор лежит на стороне
/// chat protocol (Block 8.6 will introduce MIME-typed payload framing).
///
/// F-CLIENT-FACADE-1 session 5: try MLS decrypt if group is Some, otherwise
/// UTF-8 lossy fallback. Transitional behaviour for session 5; tightens to
/// strict-MLS-only in session 7+.
async fn decrypt_text_with_fallback(
    core: &Arc<ClientCore>,
    group: Option<&Arc<TokioMutex<UmbrellaGroup>>>,
    ciphertext: &[u8],
) -> String {
    if let Some(group_arc) = group {
        let mut g = group_arc.lock().await;
        match g.process_incoming(core.mls_provider.as_ref(), ciphertext) {
            Ok(IncomingMessage::Application { payload, .. }) => {
                return String::from_utf8_lossy(&payload).into_owned();
            }
            Ok(_other) => {
                tracing::debug!(
                    "fetch_mls_inbox: non-Application MLS message variant, falling back to UTF-8 lossy"
                );
            }
            Err(_) => {
                tracing::debug!(
                    "fetch_mls_inbox: MLS decrypt failed on payload, falling back to UTF-8 lossy (legacy/test fixture path)"
                );
            }
        }
    }
    String::from_utf8_lossy(ciphertext).into_owned()
}

